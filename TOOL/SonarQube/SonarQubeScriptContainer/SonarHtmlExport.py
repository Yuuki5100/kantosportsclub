def get_latest_analysis(
    session: Session,
    base_url: str,
    project_key: str,
    branch: Optional[str],
    timeout: int,
    verify_ssl: bool,
    labels: Dict[str, str],
) -> Dict[str, str]:
    data = api_get(
        session,
        base_url,
        "/api/project_analyses/search",
        with_branch({"project": project_key, "ps": 1}, branch),
        timeout,
        verify_ssl,
    )
    latest = {"date": txt(labels, "na"), "version": txt(labels, "na")}
    analyses = (data or {}).get("analyses", [])
    if analyses:
        first = analyses[0]
        latest["date"] = format_sonar_datetime(first.get("date"), labels)
        for event in first.get("events", []):
            if event.get("category") == "VERSION":
                latest["version"] = event.get("name", txt(labels, "na"))
                break
    return latest


def get_main_branch_name(
    session: Session,
    base_url: str,
    project_key: str,
    timeout: int,
    verify_ssl: bool,
) -> str:
    data = api_get(
        session,
        base_url,
        "/api/project_branches/list",
        {"project": project_key},
        timeout,
        verify_ssl,
    )
    for branch_data in (data or {}).get("branches", []):
        if branch_data.get("isMain"):
            return branch_data.get("name", "main")
    return "main"


