def normalize_issue_type(raw_type: Any) -> str:
    value = str(raw_type or "UNKNOWN").strip().upper()
    mapping = {
        "VULNERABILITY": "SECURITY",
        "CODE_SMELL": "MAINTAINABILITY",
    }
    return mapping.get(value, value)


def normalize_severity(raw_severity: Any) -> str:
    value = str(raw_severity or "UNKNOWN").strip().upper()
    mapping = {
        "CRITICAL": "HIGH",
        "MAJOR": "MEDIUM",
        "MINOR": "LOW",
    }
    return mapping.get(value, value)


