def main() -> int:
    parser = argparse.ArgumentParser(description="Generate an HTML report from SonarQube.")
    parser.add_argument("--host", default="http://localhost:9000", help="SonarQube host URL")
    parser.add_argument("--project", required=True, help="SonarQube project key")
    parser.add_argument("--token", required=True, help="SonarQube user token")
    parser.add_argument("--branch", default=None, help="Branch name (optional)")
    parser.add_argument("--output", default="sonar-report.html", help="Output HTML file path")
    parser.add_argument("--timeout", type=int, default=20, help="HTTP timeout in seconds")
    parser.add_argument(
        "--max-issues",
        type=int,
        default=0,
        help="Max issues to render (0 = all, useful for huge projects)",
    )
    parser.add_argument(
        "--insecure",
        action="store_true",
        help="Disable TLS cert verification (useful for self-signed local HTTPS)",
    )
    parser.add_argument(
        "--jp",
        action="store_true",
        help="Render report labels in Japanese",
    )
    args = parser.parse_args()
    labels = labels_for(args.jp)

    verify_ssl = not args.insecure
    if args.insecure:
        requests.packages.urllib3.disable_warnings()  # type: ignore[attr-defined]

    session = requests.Session()
    session.auth = (args.token, "")
    session.headers.update({"Accept": "application/json"})

    try:
        auth_data = api_get(
            session,
            args.host,
            "/api/authentication/validate",
            timeout=args.timeout,
            verify_ssl=verify_ssl,
        )
        if not auth_data or not auth_data.get("valid", False):
            print(txt(labels, "msg_auth_failed"), file=sys.stderr)
            return 2

        try:
            health_data = api_get(
                session,
                args.host,
                "/api/system/health",
                timeout=args.timeout,
                verify_ssl=verify_ssl,
            )
            server_health = (health_data or {}).get("health", "UNKNOWN")
        except Exception:
            server_health = "UNKNOWN"

        branch_name = args.branch or get_main_branch_name(
            session, args.host, args.project, args.timeout, verify_ssl
        )

        measures = get_measures(
            session, args.host, args.project, args.branch, args.timeout, verify_ssl
        )
        qg_data = get_quality_gate(
            session, args.host, args.project, args.branch, args.timeout, verify_ssl
        )
        latest = get_latest_analysis(
            session, args.host, args.project, args.branch, args.timeout, verify_ssl, labels
        )
        issues = get_all_unresolved_issues(
            session, args.host, args.project, args.branch, args.timeout, verify_ssl
        )
        duplicated_files = get_duplicated_files(
            session, args.host, args.project, args.branch, args.timeout, verify_ssl
        )

        qg_raw = (
            qg_data.get("projectStatus", {}).get("status")
            if qg_data
            else measures.get("alert_status")
        )

        generated_at = dt.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        report_html = build_html(
            project_key=args.project,
            branch_name=branch_name,
            quality_gate_raw=qg_raw,
            quality_gate_data=qg_data,
            measures=measures,
            latest_analysis=latest,
            issues=issues,
            duplicated_files=duplicated_files,
            server_health=server_health,
            generated_at=generated_at,
            max_issues=args.max_issues,
            use_jp=args.jp,
        )

        out_path = Path(args.output).resolve()
        out_path.write_text(report_html, encoding="utf-8")
        print(txt(labels, "msg_report_generated").format(path=out_path))
        return 0

    except RequestException as ex:
        print(txt(labels, "msg_http_error").format(error=ex), file=sys.stderr)
        return 1
    except Exception as ex:
        print(txt(labels, "msg_unexpected_error").format(error=ex), file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
