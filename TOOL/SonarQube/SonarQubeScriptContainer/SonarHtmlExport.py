def localize_issue_type(value: Any, use_jp: bool) -> str:
    key = normalize_issue_type(value)
    return ISSUE_TYPE_LABELS["jp" if use_jp else "en"].get(key, key)


def localize_severity(value: Any, use_jp: bool) -> str:
    key = normalize_severity(value)
    return SEVERITY_LABELS["jp" if use_jp else "en"].get(key, key)


def localize_issue_status(value: Any, use_jp: bool) -> str:
    key = str(value or "UNKNOWN").strip().upper()
    return ISSUE_STATUS_LABELS["jp" if use_jp else "en"].get(key, key)


def normalized_issue_severity_for_sort(raw_severity: Any) -> str:
    return normalize_severity(raw_severity)


def issue_sort_key(issue: Dict[str, Any]) -> Any:
    severity_order = {
        "BLOCKER": 0,
        "HIGH": 1,
        "MEDIUM": 2,
        "LOW": 3,
        "INFO": 4,
        "UNKNOWN": 5,
    }
    severity = normalized_issue_severity_for_sort(issue.get("severity", "UNKNOWN"))
    issue_type = normalize_issue_type(issue.get("type", "UNKNOWN"))
    component = str(issue.get("component", ""))
    line = issue.get("line", 0)
    try:
        line_number = int(line)
    except Exception:
        line_number = 0
    return (
        severity_order.get(severity, 99),
        issue_type,
        component,
        line_number,
        str(issue.get("message", "")),
    )


def build_issue_group_table_rows(
    items: List[Dict[str, Any]],
    labels: Dict[str, str],
    use_jp: bool,
) -> str:
    rows: List[str] = []
    for issue in sorted(items, key=issue_sort_key):
        component = str(issue.get("component", ""))
        file_path = component.split(":", 1)[1] if ":" in component else component
        issue_type_raw = normalize_issue_type(issue.get("type", "UNKNOWN"))
        severity_raw = normalize_severity(issue.get("severity", "UNKNOWN"))
        severity_css = f"sev-{severity_raw.lower()}"
        status_raw = issue.get("status", issue.get("issueStatus", "UNKNOWN"))
        line_value = issue.get("line", txt(labels, "na"))
        if line_value in (None, ""):
            line_value = txt(labels, "na")

        rows.append(
            f"""
            <tr>
              <td>{escape(localize_issue_type(issue_type_raw, use_jp))}</td>
              <td class="{severity_css}">{escape(localize_severity(severity_raw, use_jp))}</td>
              <td>{escape(file_path)}</td>
              <td>{escape(line_value)}</td>
              <td>{escape(issue.get("message", ""))}</td>
              <td>{escape(localize_issue_status(status_raw, use_jp))}</td>
              <td>{escape(issue.get("rule", txt(labels, "na")))}</td>
            </tr>
            """
        )

    if not rows:
        return (
            f'<tr><td colspan="7" class="muted">'
            f'{escape(txt(labels, "no_issues_in_group"))}</td></tr>'
        )
    return "".join(rows)


def build_collapsible_issue_groups(
    issues: List[Dict[str, Any]],
    group_by: str,
    labels: Dict[str, str],
    use_jp: bool,
) -> str:
    grouped: Dict[str, List[Dict[str, Any]]] = {}
    if group_by == "severity":
        for issue in issues:
            key = normalize_severity(issue.get("severity", "UNKNOWN"))
            grouped.setdefault(key, []).append(issue)
        ordered_keys = ["BLOCKER", "HIGH", "MEDIUM", "LOW", "INFO", "UNKNOWN"]
    else:
        for issue in issues:
            key = normalize_issue_type(issue.get("type", "UNKNOWN"))
            grouped.setdefault(key, []).append(issue)
        ordered_keys = ["BUG", "SECURITY", "MAINTAINABILITY", "UNKNOWN"]

    html_chunks: List[str] = []
    for key in ordered_keys:
        if key not in grouped:
            continue
        count = len(grouped[key])
        detail_id = f"{group_by}-{key.lower()}"
        table_rows = build_issue_group_table_rows(grouped[key], labels, use_jp)
        group_label = (
            localize_severity(key, use_jp)
            if group_by == "severity"
            else localize_issue_type(key, use_jp)
        )

        html_chunks.append(
            f"""
            <details class="issue-group" id="{escape(detail_id)}">
              <summary>
                <span>{escape(group_label)}</span>
                <span class="summary-count">{count}</span>
              </summary>
              <div class="group-table-wrapper">
                <table>
                  <thead>
                    <tr>
                      <th>{escape(txt(labels, "col_type"))}</th>
                      <th>{escape(txt(labels, "col_severity"))}</th>
                      <th>{escape(txt(labels, "col_file"))}</th>
                      <th>{escape(txt(labels, "col_line"))}</th>
                      <th>{escape(txt(labels, "col_message"))}</th>
                      <th>{escape(txt(labels, "col_status"))}</th>
                      <th>{escape(txt(labels, "col_rule"))}</th>
                    </tr>
                  </thead>
                  <tbody>
                    {table_rows}
                  </tbody>
                </table>
              </div>
            </details>
            """
        )

    extra_groups = sorted([k for k in grouped.keys() if k not in ordered_keys])
    for key in extra_groups:
        count = len(grouped[key])
        detail_id = f"{group_by}-{key.lower()}"
        table_rows = build_issue_group_table_rows(grouped[key], labels, use_jp)
        group_label = (
            localize_severity(key, use_jp)
            if group_by == "severity"
            else localize_issue_type(key, use_jp)
        )

        html_chunks.append(
            f"""
            <details class="issue-group" id="{escape(detail_id)}">
              <summary>
                <span>{escape(group_label)}</span>
                <span class="summary-count">{count}</span>
              </summary>
              <div class="group-table-wrapper">
                <table>
                  <thead>
                    <tr>
                      <th>{escape(txt(labels, "col_type"))}</th>
                      <th>{escape(txt(labels, "col_severity"))}</th>
                      <th>{escape(txt(labels, "col_file"))}</th>
                      <th>{escape(txt(labels, "col_line"))}</th>
                      <th>{escape(txt(labels, "col_message"))}</th>
                      <th>{escape(txt(labels, "col_status"))}</th>
                      <th>{escape(txt(labels, "col_rule"))}</th>
                    </tr>
                  </thead>
                  <tbody>
                    {table_rows}
                  </tbody>
                </table>
              </div>
            </details>
            """
        )

    if not html_chunks:
        return f'<p class="muted">{escape(txt(labels, "no_unresolved_issues"))}</p>'
    return "".join(html_chunks)


def get_measures(
    session: Session,
    base_url: str,
    project_key: str,
    branch: Optional[str],
    timeout: int,
    verify_ssl: bool,
) -> Dict[str, str]:
    metric_keys = ",".join(
        [
            "alert_status",
            "ncloc",
            "bugs",
            "vulnerabilities",
            "code_smells",
            "security_hotspots",
            "coverage",
            "duplicated_lines_density",
            "reliability_rating",
            "security_rating",
            "sqale_rating",
        ]
    )
    data = api_get(
        session,
        base_url,
        "/api/measures/component",
        with_branch({"component": project_key, "metricKeys": metric_keys}, branch),
        timeout,
        verify_ssl,
    )
    measures: Dict[str, str] = {}
    for measure in (data or {}).get("component", {}).get("measures", []):
        metric = measure.get("metric")
        value = measure.get("value")
        if metric is not None and value is not None:
            measures[str(metric)] = str(value)
    return measures


