def build_url(base_url: str, path: str) -> str:
    return urljoin(base_url.rstrip("/") + "/", path.lstrip("/"))


def api_get(
    session: Session,
    base_url: str,
    path: str,
    params: Optional[Dict[str, Any]] = None,
    timeout: int = 20,
    verify_ssl: bool = True,
) -> Optional[Dict[str, Any]]:
    url = build_url(base_url, path)
    response = session.get(url, params=params, timeout=timeout, verify=verify_ssl)

    if response.status_code == 404:
        return None

    response.raise_for_status()
    return response.json()


def with_branch(params: Dict[str, Any], branch: Optional[str]) -> Dict[str, Any]:
    if branch:
        params["branch"] = branch
    return params


def escape(value: Any) -> str:
    return html.escape("" if value is None else str(value))


def labels_for(use_jp: bool) -> Dict[str, str]:
    return I18N_TEXT["jp" if use_jp else "en"]


def txt(labels: Dict[str, str], key: str) -> str:
    return labels.get(key, key)


def format_percent(value: Any, labels: Dict[str, str]) -> str:
    if value is None:
        return txt(labels, "na")
    text = str(value).strip()
    if not text:
        return txt(labels, "na")
    if text.endswith("%"):
        return text
    return f"{text}%"


def rating_to_grade(value: Any) -> str:
    try:
        grade_map = {1: "A", 2: "B", 3: "C", 4: "D", 5: "E"}
        return grade_map.get(int(float(value)), "N/A")
    except Exception:
        return "N/A"


def rating_display(value: Any, labels: Dict[str, str]) -> str:
    grade = rating_to_grade(value)
    if grade == "N/A" or value is None:
        return txt(labels, "na")
    return f"{grade} ({value})"


def format_sonar_datetime(raw: Optional[str], labels: Dict[str, str]) -> str:
    if not raw:
        return txt(labels, "na")
    for fmt in ("%Y-%m-%dT%H:%M:%S%z", "%Y-%m-%dT%H:%M:%S.%f%z"):
        try:
            return dt.datetime.strptime(raw, fmt).strftime("%Y-%m-%d %H:%M:%S %Z")
        except ValueError:
            pass
    return raw


